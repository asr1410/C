#include<stdio.h>
int main()
{
    int a;
    int b;
    a = 1;
    b = 1;
    printf("b = %d\n", b);
    printf("a = %d\n", a);
    printf("a | b = %d\n", a|b);
    printf("a & b = %d\n", a&b);
    printf("a ^ b = %d\n", a^b);
    printf("!a = %d\n", !a);
    printf("!b = %d\n", !b);
    return 0;
}